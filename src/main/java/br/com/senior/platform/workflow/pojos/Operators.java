package br.com.senior.platform.workflow.pojos;

/**
 * Operadores de comparação de filtro
 */
public enum Operators {
    /**
     * Igual
     */
    EQ,
    /**
     * Diferente
     */
    NE,
    /**
     * Menor
     */
    LT,
    /**
     * Maior
     */
    GT,
    /**
     * Menor ou igual
     */
    LOE,
    /**
     * Maior ou igual
     */
    GOE,
    /**
     * Contém
     */
    CONTAINS
}
